// Example Variable
let objName = '';

// Traditional
function funcName() {
    objName = 'Function';
}

// Arrow Notation with Brackets
const funcName = () => {
    objName = 'Function';
}

// Inline Arrow Notation
const funcName = () => objName = 'Function';

// Function Expression
const funcName = function() {
    objName = 'Function';
}